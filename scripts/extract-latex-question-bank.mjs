import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const options = parseArgs(process.argv.slice(2));
const sourceSlug = slugify(options.slug || options.source || "question-bank");
const sourceName = options.name || "题库";
const idPrefix = options.idPrefix || (sourceSlug === "question-bank" ? "catalog" : sourceSlug);
const inputPath = path.resolve(
  options.input || options._[0] || path.join(projectRoot, "data", "question-banks", sourceSlug, "source.tex")
);
const sourceDir = path.join(projectRoot, "data", "question-banks", sourceSlug);
const jsOutputPath = path.join(sourceDir, "problems.js");
const jsonOutputPath = path.join(sourceDir, "problems.json");
const importedAt = new Date().toISOString();

if (!fs.existsSync(inputPath)) {
  console.error(`Input TeX file not found: ${inputPath}`);
  console.error("Usage: node scripts/extract-latex-question-bank.mjs --input /path/book.tex --slug book-slug --name 题库名称");
  process.exit(1);
}

const tex = fs.readFileSync(inputPath, "utf8");
const body = tex.slice(Math.max(0, tex.indexOf("\\begin{document}")));
const lines = body.split(/\r?\n/);
const appendixExerciseTitles = [
  "十枚硬币奇偶性",
  "混合硬币奇偶性",
  "圆内随机点距离",
  "阶乘末尾零",
  "阶乘末位非零数字",
  "连续双六等待时间",
  "随机三角形包含圆心",
  "HTT 首次出现等待时间",
  "条件正态象限概率",
  "正态半径条件期望",
  "菱形区域整数点计数",
  "偏置多边形随机游走",
  "绳上两点距离分布",
  "有序整数分拆极限",
  "根式幂个位数字",
  "均匀次序统计协方差",
  "相关正态条件概率",
  "余弦无理性证明",
  "三人硬币转移链",
  "球面短弧期望",
  "数字乘积最小数",
  "男女相邻对数期望"
];

let chapter = "";
let section = "";
let subsection = "";
let problemIndex = 0;
const problems = [];

for (let index = 0; index < lines.length; index += 1) {
  chapter = readHeading(lines[index], "chapter") || chapter;
  section = readHeading(lines[index], "section") || section;
  subsection = readHeading(lines[index], "subsection") || subsection;
  if (!/\\begin\{problembox\}/.test(lines[index])) continue;

  const prompt = collectEnvironment(lines, index, "problembox");
  const solutionStart = findLine(lines, prompt.end + 1, (line) => /\\solution\b/.test(line));
  const solutionEnd = findLine(lines, solutionStart + 1, (line) => (
    /\\(?:chapter|section|subsection)\{/.test(line) || /\\begin\{problembox\}/.test(line)
  ));
  const rawSolution = solutionStart >= 0
    ? lines.slice(solutionStart + 1, solutionEnd >= 0 ? solutionEnd : lines.length).join("\n")
    : "";

  problemIndex += 1;
  problems.push(makeProblem({
    id: `${idPrefix}-problem-${String(problemIndex).padStart(3, "0")}`,
    title: subsection || `题库题目 ${problemIndex}`,
    chapter,
    section,
    prompt: prompt.content,
    solution: rawSolution,
    category: inferCategory(chapter, section, prompt.content),
    tags: [chapter, section].filter(Boolean)
  }));
  index = prompt.end;
}

problems.push(...extractAppendixExercises(lines, problems.length));

fs.mkdirSync(path.dirname(jsOutputPath), { recursive: true });
fs.writeFileSync(jsOutputPath, [
  "// Generated by scripts/extract-latex-question-bank.mjs.",
  `window.quantProblemCatalog = ${JSON.stringify(problems, null, 2)};`,
  ""
].join("\n"));
