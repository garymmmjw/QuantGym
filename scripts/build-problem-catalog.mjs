import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const sourceRoot = path.join(projectRoot, "data", "question-banks");
const manifestPath = path.join(sourceRoot, "catalog-manifest.json");
const jsonOutputPath = path.join(projectRoot, "data", "problem-catalog.json");
const jsOutputPath = path.join(projectRoot, "data", "problem-catalog.js");

const manifest = readJson(manifestPath, {
  version: 1,
  compiledCatalog: "../problem-catalog.json",
  sources: []
});
const sources = resolveSources(manifest);
const mergedProblems = [];
const seenIds = new Set();
let activeSourceCount = 0;

sources.forEach((source) => {
  const sourceSlug = String(source.slug || "").trim();
  if (!sourceSlug) return;
  if (source.disabled || source.excludeFromCatalog) {
    source.lastBuiltAt = new Date().toISOString();
    return;
  }
  activeSourceCount += 1;

  const problemPath = path.join(sourceRoot, source.problemFile || `${sourceSlug}/problems.json`);
  if (!fs.existsSync(problemPath)) {
    console.warn(`Skipped missing problem source: ${problemPath}`);
    source.problemCount = 0;
    return;
  }

  const payload = readJson(problemPath, {});
  const problems = Array.isArray(payload) ? payload : payload.problems;
  if (!Array.isArray(problems)) {
    console.warn(`Skipped invalid problem source: ${problemPath}`);
    source.problemCount = 0;
    return;
  }

  source.problemCount = problems.length;
  source.lastBuiltAt = new Date().toISOString();
  problems.forEach((problem) => {
    if (!problem || typeof problem !== "object") return;
    const id = String(problem.id || "").trim();
    if (!id || seenIds.has(id)) return;
    seenIds.add(id);
    mergedProblems.push({
      ...problem,
      source: problem.source || sourceSlug,
      sourceUrl: problem.sourceUrl || source.name || "",
      sourceType: problem.sourceType || "book",
      bookSlug: problem.bookSlug || sourceSlug,
      bookName: problem.bookName || source.name || sourceSlug,
      visibility: problem.visibility || "public"
    });
  });
});

fs.mkdirSync(path.dirname(jsonOutputPath), { recursive: true });
fs.writeFileSync(jsonOutputPath, `${JSON.stringify({ problems: mergedProblems }, null, 2)}\n`);
fs.writeFileSync(jsOutputPath, [
  "// Generated by scripts/build-problem-catalog.mjs.",
  `window.quantProblemCatalog = ${JSON.stringify(mergedProblems, null, 2)};`,
  ""
].join("\n"));
fs.writeFileSync(manifestPath, `${JSON.stringify({ ...manifest, sources }, null, 2)}\n`);

console.log(`Built ${mergedProblems.length} catalog problems from ${activeSourceCount} active source(s).`);
console.log(`Wrote ${jsonOutputPath}`);
console.log(`Wrote ${jsOutputPath}`);

function resolveSources(manifest) {
  if (Array.isArray(manifest.sources) && manifest.sources.length) return manifest.sources;
  if (!fs.existsSync(sourceRoot)) return [];
  return fs.readdirSync(sourceRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => {
      const metadata = readJson(path.join(sourceRoot, entry.name, "metadata.json"), {});
      return {
        slug: entry.name,
        name: metadata.name || entry.name,
        type: metadata.type || "latex",
        problemFile: `${entry.name}/problems.json`,
        browserFile: `${entry.name}/problems.js`
      };
    });
}

function readJson(filePath, fallback) {
  if (!fs.existsSync(filePath)) return fallback;
  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch {
    return fallback;
  }
}
