import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const outputDir = path.resolve(projectRoot, process.env.QUANTGYM_WEB_DIST || "dist");
const strict = process.argv.includes("--strict") || process.env.QUANTGYM_WEB_STRICT === "1";

const webConfig = {
  cloudApiEndpoint: value("QUANTGYM_WEB_API_ENDPOINT"),
  llmEndpoint: value("QUANTGYM_WEB_LLM_ENDPOINT"),
  llmModel: value("QUANTGYM_WEB_LLM_MODEL") || "gpt-5-nano",
  googleClientId: value("QUANTGYM_WEB_GOOGLE_CLIENT_ID"),
  googleLoginEnabled: boolValue("QUANTGYM_WEB_GOOGLE_LOGIN_ENABLED")
};

if (strict) {
  requireHttps("QUANTGYM_WEB_API_ENDPOINT", webConfig.cloudApiEndpoint);
  requireHttps("QUANTGYM_WEB_LLM_ENDPOINT", webConfig.llmEndpoint);
}

assertSafeOutputDir(outputDir);
fs.rmSync(outputDir, { recursive: true, force: true });
fs.mkdirSync(outputDir, { recursive: true });

copyFile("index.html");
copyFile("app.js");
copyFile("styles.css");
copyFile(path.join("data", "problem-catalog.js"));
copyFile(path.join("data", "leetcode-hot-100.js"));
copyDir(path.join("assets", "generated"));
writeConfig();

console.log(`Built static site in ${path.relative(projectRoot, outputDir) || outputDir}`);
if (!webConfig.cloudApiEndpoint || !webConfig.llmEndpoint) {
  console.warn("Warning: generated config.js has empty API/LLM endpoints and will fall back to local development URLs.");
}

function value(name) {
  return String(process.env[name] || "").trim();
}

function boolValue(name) {
  return ["1", "true", "yes", "on"].includes(value(name).toLowerCase());
}

function requireHttps(name, endpoint) {
  if (!endpoint) throw new Error(`${name} is required when --strict is used`);
  if (!endpoint.startsWith("https://")) throw new Error(`${name} must start with https:// for beta deployment`);
}

function assertSafeOutputDir(dir) {
  const relative = path.relative(projectRoot, dir);
  if (!relative || relative === "." || relative.startsWith("..") || path.isAbsolute(relative)) {
    throw new Error("QUANTGYM_WEB_DIST must point to a directory inside the project, such as dist");
  }
  if ([".", "..", "/", "\\"].includes(dir)) {
    throw new Error("Refusing to clear an unsafe output directory");
  }
}

function copyFile(relativePath) {
  const from = path.join(projectRoot, relativePath);
  const to = path.join(outputDir, relativePath);
  if (!fs.existsSync(from)) throw new Error(`Missing required file: ${relativePath}`);
  fs.mkdirSync(path.dirname(to), { recursive: true });
  fs.copyFileSync(from, to);
}

function copyDir(relativePath) {
  const from = path.join(projectRoot, relativePath);
  const to = path.join(outputDir, relativePath);
  if (!fs.existsSync(from)) throw new Error(`Missing required directory: ${relativePath}`);
  fs.cpSync(from, to, { recursive: true });
}

function writeConfig() {
  const configPath = path.join(outputDir, "config.js");
  const content = [
    "// Generated by scripts/build-static-site.mjs.",
    "window.QUANTGYM_CONFIG = " + JSON.stringify(webConfig, null, 2) + ";",
    ""
  ].join("\n");
  fs.writeFileSync(configPath, content);
}
